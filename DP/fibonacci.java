import java.util.*;
public class fibonacci {
    public static int fiboseries(int n){
        if(n==1||n==0){
            return n;
        }
        return 0;
    }
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int number=sc.nextInt();
        fiboseries(number);
        sc.close();;;;;;;;;;
    }
    
}
